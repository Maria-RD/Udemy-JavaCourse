package section5;

public class SentenciaIfElse {
    public static void main(String args[]){
        var condicion = true;
        
        // Ejemplo sencillo de condición verdadera o falsa
        if(condicion) {
            System.out.println("Condición verdadera");
        } else {
            System.out.println("Condición falsa");
        }
    }
}
